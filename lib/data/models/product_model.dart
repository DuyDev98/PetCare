class Product {
  final String id;
  final String name;
  final String image;
  final double price;
  final double oldPrice;
  final String category;
  final int sold;
  final String description;

  Product({
    required this.id,
    required this.name,
    required this.image,
    required this.price,
    required this.oldPrice,
    required this.category,
    required this.sold,
    required this.description,
  });
}